public class Loop_practice_14 {
    public static void main(String[] args)
    {
       
        int j , i ;
        for(i=1 ; i<=5 ;i++)
        {
            System.out.println("*");
            for(j= 1 ; j<=i ; j++)
            {
                System.out.print(" ");
            }
            System.out.println("*");
        }
      
        
    }
}
